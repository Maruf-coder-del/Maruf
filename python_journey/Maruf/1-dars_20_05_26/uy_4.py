# "Raqamlar sehrgari" o'yini:
# 1 dan 10 gacha son o'ylash.
# O'sha son bilan turli matematik amallar bajarish:
# - Kvadrati
# - Kubi
# - 2 ga ko'paytirib, 5 qo'shish
# - 3 bo'lgandagi qoldiq
#
# Natijani foydalnuvchiga sirli tarzda ko'rsatish.

son = 7
kvadrat = son ** 2
kub = son ** 3
natija = (son * 2 + 5)
qoldiq = son % 3
print(f"Sirli natija: {natija}", f"Kvadrati: {kvadrat}", f"Kubi: {kub}", f"3 ga bo'lgandagi qoldiq: {qoldiq}")
