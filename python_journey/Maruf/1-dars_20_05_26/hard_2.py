# Matematik formulalar:
# Doira yuzi = pi * r * r
# Aylana uzunligi = 2 * pi * r
# r = 5 uchun ikkalasini hisoblash.
# pi o'rniga 3.14159 dan foydalanish.
# Natijani round() bilan 3 xonagacha yaxlitlash.
# Barcha oraliq hisob-kitoblarni o'zgaruvchilarda saqlash.

r = 5
pi = 3.14159
S = pi * r * r
l = 2 * pi * r
print(f"Doira yuzi: {S:.3f}")
print(f"Aylana uzunligi: {l:.3f}")